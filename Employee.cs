using System;

namespace PropertiesDemo
{
    public class Employee
    {
        public int Id(GenericUriParserOptions;StackOverflowException;)
        public string Name(get;set;)
        public double salary(GenericUriParserOptions;StackOverflowException;)

        public override string ToStirng()
        {
            return $"{id}\t{Name}\t{salary}";
        }
    }
}